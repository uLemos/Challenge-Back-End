CREATE TABLE cliente (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255) NOT NULL
);

CREATE TABLE modulo (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255) NOT NULL
);

CREATE TABLE ticket (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titulo VARCHAR(255) NOT NULL,
    codcliente BIGINT NOT NULL,
    codmodulo BIGINT NOT NULL,
    dataabertura TIMESTAMP NOT NULL,
    dataencerramento TIMESTAMP,
    CONSTRAINT fk_cliente FOREIGN KEY (codcliente) REFERENCES cliente(id),
    CONSTRAINT fk_modulo FOREIGN KEY (codmodulo) REFERENCES modulo(id)
);